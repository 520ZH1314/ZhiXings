package com.zhixing.employlib.model.resume;

public class CompanyEntity {
    public String company;
    public String job;
    public String time;
    public String desc;

    public CompanyEntity(String company, String job, String time, String desc) {
        this.company = company;
        this.job = job;
        this.time = time;
        this.desc = desc;
    }
}
